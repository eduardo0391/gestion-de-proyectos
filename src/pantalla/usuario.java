    public void actualizar()
    {
        modelo = (DefaultTableModel) this.tabla.getModel();
        this.limpiar();
        Object[] usuarios= unaEmpresa.getUsuarios().values().toArray();
        String fila[]=new String[2];
        for (int i= 0; i<(usuarios.length); i++)
        {          
            fila[0]= ((Usuario)usuarios[i]).getNombre();
            if (((Usuario)usuarios[i]).getEjecucion())
            fila[1]= "Ejecucion";
            else
            fila[1]= "Formulacion";
            modelo.addRow(fila);
        }
    }
    public void limpiarVentanas()
    {
       usuario.setText("");
       contra.setText("");
       tipo.getItemAt(0);
    }

    public void limpiar()
    {
        modelo = (DefaultTableModel) this.tabla.getModel();
        while(modelo.getRowCount()>0){
        modelo.removeRow(0);
     }
    }
