 private void IniciarArbol(String id)
     {
        this.Jtree1.removeAll();
        Map <Integer, Partida> par = new HashMap();
        Partida unaPartida = new Partida();
        Map arregloproyecto = new HashMap();
        arregloproyecto.putAll(unaEmpresa.getProyectos());
        Proyecto raiz = (Proyecto)arregloproyecto.get(id);
        DefaultMutableTreeNode top = new DefaultMutableTreeNode(raiz.getNombre());
        DefaultTreeModel modelo = new DefaultTreeModel(top);
        Object[] f = raiz.getPartidas().values().toArray();
        this.Jtree1.setModel(modelo);
        for (int i = 0; i< f.length; i++)
        {
            unaPartida = ((Partida)f [i]);
            if (unaPartida.getPadre()==null)
            {
            par.put(i, unaPartida);
            }

        }
        Object [] x = null;
        x = par.values().toArray();
        cargarArbol(x, top);
    }

    private void cargarArbol(Object[] f, DefaultMutableTreeNode nodo) {
        for (int i = 0; i < f.length; i++) {
            if (((Partida)f[i]).esDescriptiva() )
            {
                 DefaultMutableTreeNode nodoAux = new DefaultMutableTreeNode(f[i]);
                 nodo.add(nodoAux);
                 cargarArbol(((PartidaDescriptiva)f[i]).getHijos().values().toArray(), nodoAux);
            }
            else{
                if (((Partida)f[i]).esDescriptiva() == false)
                { DefaultMutableTreeNode nodoAux = new DefaultMutableTreeNode(f[i]);
                    nodo.add(nodoAux);}
                }
        }}
